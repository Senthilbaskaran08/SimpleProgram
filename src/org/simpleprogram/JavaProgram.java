package org.simpleprogram;

public class JavaProgram {
static void program() {
	String s = "Senthil";
	System.out.println(s);
}
static void program2() {
	String s = "Senthil2";
	System.out.println(s);
}
public static void main(String[] args) {
	JavaProgram obj = new JavaProgram();
	obj.program();
	obj.program2();
}
}	

 